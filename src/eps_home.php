    &emsp;情報科学科では、情報技術コースの中に「総合教育プログラム」と「応⽤教育プログラ
ム」を設けています。「総合教育プログラム」では、情報科学・情報技術の基礎をしっかり
と⾝につけ、ソフトウェア開発やハードウェア開発、情報システムの設計・開発等にかかわ
